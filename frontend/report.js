/* ════════════════════════════════════════════════
   PROFESSIONAL PDF REPORT GENERATOR
   Multi-page consulting-style report
   ════════════════════════════════════════════════ */

async function generateProfessionalReport() {
  if (!state.results) {
    toast('Calculate results before generating report.', 'warning');
    return;
  }

  if (!window.jspdf) {
    toast('PDF library not available.', 'error');
    return;
  }

  const button = document.getElementById('btn-export-pdf');
  setLoading(button, true);
  toast('Generating professional report...', 'info', 2000);

  try {
    const { jsPDF } = window.jspdf;
    const pdf = new jsPDF('p', 'mm', 'a4');
    const pageWidth = pdf.internal.pageSize.getWidth();
    const pageHeight = pdf.internal.pageSize.getHeight();
    const margin = 15;
    const contentWidth = pageWidth - (margin * 2);

    // Extract chart images
    const chartImages = await extractChartImages();

    // Page 1: Cover Page
    addCoverPage(pdf, pageWidth, pageHeight);

    // Page 2: Executive Summary
    pdf.addPage();
    addExecutiveSummary(pdf, margin, contentWidth);

    // Page 3: Criteria & Weights
    pdf.addPage();
    addCriteriaWeights(pdf, margin, contentWidth);

    // Page 4: Options & Scores
    pdf.addPage();
    addOptionsScores(pdf, margin, contentWidth);

    // Page 5: Charts
    if (chartImages.bar || chartImages.radar) {
      pdf.addPage();
      addChartsPage(pdf, margin, contentWidth, chartImages);
    }

    // Page 6: Detailed Insights
    if (state.insights) {
      pdf.addPage();
      addDetailedInsights(pdf, margin, contentWidth);
    }

    // Page 7: Sensitivity Analysis (if available)
    if (sensitivityState.active && sensitivityState.original && sensitivityState.current) {
      pdf.addPage();
      addSensitivitySummary(pdf, margin, contentWidth);
    }

    // Page 8: Scenario Simulation (if available)
    if (scenarioState.active && scenarioState.scenarios.length) {
      pdf.addPage();
      addScenarioSummary(pdf, margin, contentWidth);
    }

    // Save PDF
    const safeTitle = (state.decision.title || 'decision-report')
      .toLowerCase()
      .replace(/[^a-z0-9]+/g, '-')
      .replace(/^-|-$/g, '');
    
    pdf.save(`${safeTitle}-analysis-report.pdf`);
    toast('Professional report generated successfully!', 'success');
  } catch (error) {
    console.error('PDF generation error:', error);
    toast(`Report generation failed: ${error.message}`, 'error', 5000);
  } finally {
    setLoading(button, false);
  }
}

// ════════════════════════════════════════════════
// PAGE 1: COVER PAGE
// ════════════════════════════════════════════════

function addCoverPage(pdf, pageWidth, pageHeight) {
  const centerX = pageWidth / 2;
  const centerY = pageHeight / 2;

  // Title
  pdf.setFontSize(32);
  pdf.setFont('helvetica', 'bold');
  pdf.setTextColor(30, 30, 30);
  const titleLines = pdf.splitTextToSize(state.decision.title || 'Decision Analysis', pageWidth - 40);
  pdf.text(titleLines, centerX, centerY - 40, { align: 'center' });

  // Subtitle
  pdf.setFontSize(18);
  pdf.setFont('helvetica', 'normal');
  pdf.setTextColor(100, 100, 100);
  pdf.text('Decision Analysis Report', centerX, centerY - 10, { align: 'center' });

  // Date
  pdf.setFontSize(12);
  pdf.setTextColor(120, 120, 120);
  const date = new Date().toLocaleDateString('en-US', {
    year: 'numeric',
    month: 'long',
    day: 'numeric'
  });
  pdf.text(date, centerX, centerY + 10, { align: 'center' });

  // Tagline
  pdf.setFontSize(10);
  pdf.setTextColor(150, 150, 150);
  pdf.text('Generated by Personal Decision Intelligence System', centerX, pageHeight - 20, { align: 'center' });
}

// ════════════════════════════════════════════════
// PAGE 2: EXECUTIVE SUMMARY
// ════════════════════════════════════════════════

function addExecutiveSummary(pdf, margin, contentWidth) {
  let y = margin;

  // Page title
  pdf.setFontSize(20);
  pdf.setFont('helvetica', 'bold');
  pdf.setTextColor(30, 30, 30);
  pdf.text('Executive Summary', margin, y);
  y += 12;

  // Divider
  pdf.setDrawColor(200, 200, 200);
  pdf.setLineWidth(0.5);
  pdf.line(margin, y, margin + contentWidth, y);
  y += 10;

  // Final Recommendation
  pdf.setFontSize(14);
  pdf.setFont('helvetica', 'bold');
  pdf.setTextColor(40, 40, 40);
  pdf.text('Final Recommendation', margin, y);
  y += 8;

  const winner = state.results[0];
  pdf.setFontSize(16);
  pdf.setFont('helvetica', 'bold');
  pdf.setTextColor(139, 92, 246);
  pdf.text(`→ ${winner.name}`, margin + 5, y);
  y += 6;

  pdf.setFontSize(10);
  pdf.setFont('helvetica', 'normal');
  pdf.setTextColor(80, 80, 80);
  pdf.text(`Total Score: ${winner.total_score.toFixed(1)} / 100`, margin + 5, y);
  y += 12;

  // Confidence Score
  if (confidenceState.score !== null) {
    pdf.setFontSize(14);
    pdf.setFont('helvetica', 'bold');
    pdf.setTextColor(40, 40, 40);
    pdf.text('Decision Confidence', margin, y);
    y += 8;

    const confidenceColor = confidenceState.color === 'success' ? [34, 197, 94] :
                           confidenceState.color === 'warning' ? [245, 158, 11] : [239, 68, 68];
    
    pdf.setFontSize(12);
    pdf.setFont('helvetica', 'bold');
    pdf.setTextColor(...confidenceColor);
    pdf.text(`${confidenceState.level} (${(confidenceState.score * 100).toFixed(0)}%)`, margin + 5, y);
    y += 6;

    pdf.setFontSize(10);
    pdf.setFont('helvetica', 'normal');
    pdf.setTextColor(80, 80, 80);
    const confidenceText = getConfidenceSummary();
    const confidenceLines = pdf.splitTextToSize(confidenceText, contentWidth - 10);
    pdf.text(confidenceLines, margin + 5, y);
    y += (confidenceLines.length * 5) + 8;
  }

  // Key Insights
  pdf.setFontSize(14);
  pdf.setFont('helvetica', 'bold');
  pdf.setTextColor(40, 40, 40);
  pdf.text('Key Insights', margin, y);
  y += 8;

  const keyInsights = generateKeyInsights();
  keyInsights.forEach((insight, idx) => {
    if (y > 260) return; // Prevent overflow
    
    pdf.setFontSize(10);
    pdf.setFont('helvetica', 'normal');
    pdf.setTextColor(80, 80, 80);
    pdf.text(`• ${insight}`, margin + 5, y);
    y += 6;
  });
}

