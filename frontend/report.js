// HELPER FUNCTIONS
// ════════════════════════════════════════════════

function getConfidenceSummary() {
  if (confidenceState.level === 'High') {
    return 'Excellent decision quality with strong differentiation and balanced evaluation.';
  } else if (confidenceState.level === 'Medium') {
    return 'Reasonable decision quality. Consider validating key assumptions.';
  } else {
    return 'Low confidence. Review criteria, weights, and scores before finalizing.';
  }
}

function generateKeyInsights() {
  const insights = [];
  const winner = state.results[0];
  const runnerUp = state.results[1];

  // Winner insight
  insights.push(`${winner.name} is the recommended choice with a score of ${winner.total_score.toFixed(1)}`);

  // Gap insight
  if (runnerUp) {
    const gap = winner.total_score - runnerUp.total_score;
    if (gap > 15) {
      insights.push(`Clear winner with ${gap.toFixed(1)} point lead over ${runnerUp.name}`);
    } else if (gap < 5) {
      insights.push(`Close competition with ${runnerUp.name} (${gap.toFixed(1)} point gap)`);
    }
  }

  // Criteria insight
  const topCriterion = state.decision.criteria.reduce((max, c) => 
    c.weight > max.weight ? c : max
  );
  insights.push(`Decision heavily weighted by ${topCriterion.name} (${topCriterion.weight.toFixed(1)}%)`);

  // Confidence insight
  if (confidenceState.score !== null) {
    if (confidenceState.level === 'High') {
      insights.push('High confidence - decision is robust and well-supported');
    } else if (confidenceState.level === 'Low') {
      insights.push('Low confidence - consider gathering more data');
    }
  }

  return insights.slice(0, 4);
}

