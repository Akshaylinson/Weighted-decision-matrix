// PAGE 4: OPTIONS & SCORES
// ════════════════════════════════════════════════

function addOptionsScores(pdf, margin, contentWidth) {
  let y = margin;

  // Page title
  pdf.setFontSize(20);
  pdf.setFont('helvetica', 'bold');
  pdf.setTextColor(30, 30, 30);
  pdf.text('Options & Scores', margin, y);
  y += 12;

  pdf.setDrawColor(200, 200, 200);
  pdf.setLineWidth(0.5);
  pdf.line(margin, y, margin + contentWidth, y);
  y += 10;

  // Table header
  pdf.setFontSize(10);
  pdf.setFont('helvetica', 'bold');
  pdf.setTextColor(60, 60, 60);
  pdf.text('Rank', margin, y);
  pdf.text('Option', margin + 20, y);
  pdf.text('Total Score', margin + 120, y);
  pdf.text('Visual', margin + 155, y);
  y += 6;

  pdf.setLineWidth(0.3);
  pdf.line(margin, y, margin + contentWidth, y);
  y += 6;

  // Table rows
  const maxScore = state.results[0].total_score;
  
  state.results.forEach((result, idx) => {
    if (y > 270) return;

    pdf.setFontSize(10);
    
    // Rank
    pdf.setFont('helvetica', 'bold');
    pdf.setTextColor(40, 40, 40);
    pdf.text(`#${result.rank}`, margin, y);
    
    // Option name
    pdf.setFont('helvetica', result.rank === 1 ? 'bold' : 'normal');
    if (result.rank === 1) {
      pdf.setTextColor(139, 92, 246);
    } else {
      pdf.setTextColor(40, 40, 40);
    }
    const optionText = pdf.splitTextToSize(result.name, 95);
    pdf.text(optionText[0], margin + 20, y);
    
    // Score
    pdf.setFont('helvetica', 'normal');
    pdf.setTextColor(40, 40, 40);
    pdf.text(result.total_score.toFixed(1), margin + 120, y);
    
    // Visual bar
    const barWidth = (result.total_score / maxScore) * 25;
    const barColor = result.rank === 1 ? [139, 92, 246] : [148, 163, 184];
    pdf.setFillColor(...barColor);
    pdf.rect(margin + 155, y - 3, barWidth, 4, 'F');
    
    y += 8;
  });
}

// ════════════════════════════════════════════════
// PAGE 5: CHARTS
// ════════════════════════════════════════════════

async function extractChartImages() {
  const images = {};
  
  try {
    if (state.charts.bar) {
      images.bar = state.charts.bar.toBase64Image();
    }
    if (state.charts.radar) {
      images.radar = state.charts.radar.toBase64Image();
    }
  } catch (error) {
    console.error('Chart extraction error:', error);
  }
  
  return images;
}

function addChartsPage(pdf, margin, contentWidth, chartImages) {
  let y = margin;

  // Page title
  pdf.setFontSize(20);
  pdf.setFont('helvetica', 'bold');
  pdf.setTextColor(30, 30, 30);
  pdf.text('Visual Analysis', margin, y);
  y += 12;

  pdf.setDrawColor(200, 200, 200);
  pdf.setLineWidth(0.5);
  pdf.line(margin, y, margin + contentWidth, y);
  y += 10;

  // Bar chart
  if (chartImages.bar) {
    pdf.setFontSize(12);
    pdf.setFont('helvetica', 'bold');
    pdf.setTextColor(60, 60, 60);
    pdf.text('Score Comparison', margin, y);
    y += 8;

    const chartWidth = contentWidth;
    const chartHeight = 80;
    pdf.addImage(chartImages.bar, 'PNG', margin, y, chartWidth, chartHeight);
    y += chartHeight + 10;
  }

  // Radar chart
  if (chartImages.radar && y < 200) {
    pdf.setFontSize(12);
    pdf.setFont('helvetica', 'bold');
    pdf.setTextColor(60, 60, 60);
    pdf.text('Criteria Distribution', margin, y);
    y += 8;

    const chartWidth = contentWidth;
    const chartHeight = 80;
    pdf.addImage(chartImages.radar, 'PNG', margin, y, chartWidth, chartHeight);
  }
}

