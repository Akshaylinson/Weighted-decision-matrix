// PAGE 3: CRITERIA & WEIGHTS
// ════════════════════════════════════════════════

function addCriteriaWeights(pdf, margin, contentWidth) {
  let y = margin;

  // Page title
  pdf.setFontSize(20);
  pdf.setFont('helvetica', 'bold');
  pdf.setTextColor(30, 30, 30);
  pdf.text('Evaluation Criteria & Weights', margin, y);
  y += 12;

  pdf.setDrawColor(200, 200, 200);
  pdf.setLineWidth(0.5);
  pdf.line(margin, y, margin + contentWidth, y);
  y += 10;

  // Table header
  pdf.setFontSize(10);
  pdf.setFont('helvetica', 'bold');
  pdf.setTextColor(60, 60, 60);
  pdf.text('Criterion', margin, y);
  pdf.text('Weight', margin + 120, y);
  pdf.text('Visual', margin + 150, y);
  y += 6;

  pdf.setLineWidth(0.3);
  pdf.line(margin, y, margin + contentWidth, y);
  y += 6;

  // Table rows
  state.decision.criteria.forEach((criterion, idx) => {
    if (y > 270) return; // Prevent overflow

    pdf.setFontSize(10);
    pdf.setFont('helvetica', 'normal');
    pdf.setTextColor(40, 40, 40);
    
    // Criterion name
    const nameText = pdf.splitTextToSize(criterion.name, 110);
    pdf.text(nameText[0], margin, y);
    
    // Weight
    pdf.text(`${criterion.weight.toFixed(1)}%`, margin + 120, y);
    
    // Visual bar
    const barWidth = (criterion.weight / 100) * 30;
    pdf.setFillColor(139, 92, 246);
    pdf.rect(margin + 150, y - 3, barWidth, 4, 'F');
    
    y += 8;
  });
}

